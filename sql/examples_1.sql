SELECT EMPLOYEE_ID, FIRST_NAME, DEPARTMENT_NAME
    FROM EMPLOYEES, DEPARTMENTS WHERE EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID;
    
