    FROM EMPLOYEES;

SELECT 
    SUBSTR(PHONE_NUMBER, 1, 3) AS operator,
    COUNT(*) AS total_subscribers
FROM 
    HR.EMPLOYEES GROUP BY SUBSTR(PHONE_NUMBER, 1, 3) ORDER BY operator;
    
    
